package cn.sendto.hotel.models;

import java.io.Serializable;

public class CustOrderModel implements Serializable {
	private int Customer_id;
	private int orderlist_id;
	
	
	
	public CustOrderModel() {
		super();
	}
	public CustOrderModel(int customer_id, int orderlist_id) {
		super();
		Customer_id = customer_id;
		this.orderlist_id = orderlist_id;
	}
	public int getCustomer_id() {
		return Customer_id;
	}
	public void setCustomer_id(int customer_id) {
		Customer_id = customer_id;
	}
	public int getOrderlist_id() {
		return orderlist_id;
	}
	public void setOrderlist_id(int orderlist_id) {
		this.orderlist_id = orderlist_id;
	}
	
	
}
