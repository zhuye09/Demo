package cn.sendto.hotel.models;

import java.io.Serializable;

public class OrderListModel implements Serializable {
	private int id;
	private int userId;
	private String roomid;
	private String cusName;
	private String liveDate;
	private String reallyCheckoutDate;
	private String expectCheckoutDate;
	private Float discount;
	private Float expectCost;
	private Float reallyCost;
	private int staste;
	private int days;
	private int regDays;
	private Float price;
	private  String idType;
    private  int orderlist_id;
	public OrderListModel() {
		super();
	}

	public OrderListModel(int id, int userId, String roomid, String cusName, String liveDate, String reallyCheckoutDate,
			String expectCheckoutDate, Float discount, Float expectCost, Float reallyCost, int staste, int days,
			int regDays, Float price, String idType, int orderlist_id) {
		super();
		this.id = id;
		this.userId = userId;
		this.roomid = roomid;
		this.cusName = cusName;
		this.liveDate = liveDate;
		this.reallyCheckoutDate = reallyCheckoutDate;
		this.expectCheckoutDate = expectCheckoutDate;
		this.discount = discount;
		this.expectCost = expectCost;
		this.reallyCost = reallyCost;
		this.staste = staste;
		this.days = days;
		this.regDays = regDays;
		this.price = price;
		this.idType = idType;
		this.orderlist_id = orderlist_id;
	}

	public int getOrderlist_id() {
		return orderlist_id;
	}

	public void setOrderlist_id(int orderlist_id) {
		this.orderlist_id = orderlist_id;
	}

	public Float getPrice() {
		return price;
	}

	public void setPrice(Float price) {
		this.price = price;
	}

	public String getIdType() {
		return idType;
	}

	public void setIdType(String idType) {
		this.idType = idType;
	}

	public String getRoomid() {
		return roomid;
	}


	public void setRoomid(String roomid) {
		this.roomid = roomid;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	

	public String getCusName() {
		return cusName;
	}

	public void setCusName(String cusName) {
		this.cusName = cusName;
	}

	public String getLiveDate() {
		return liveDate;
	}

	public void setLiveDate(String liveDate) {
		this.liveDate = liveDate;
	}

	public String getReallyCheckoutDate() {
		return reallyCheckoutDate;
	}

	public void setReallyCheckoutDate(String reallyCheckoutDate) {
		this.reallyCheckoutDate = reallyCheckoutDate;
	}

	public String getExpectCheckoutDate() {
		return expectCheckoutDate;
	}

	public void setExpectCheckoutDate(String expectCheckoutDate) {
		this.expectCheckoutDate = expectCheckoutDate;
	}

	public Float getDiscount() {
		return discount;
	}

	public void setDiscount(Float discount) {
		this.discount = discount;
	}

	public Float getExpectCost() {
		return expectCost;
	}

	public void setExpectCost(Float expectCost) {
		this.expectCost = expectCost;
	}

	public Float getReallyCost() {
		return reallyCost;
	}

	public void setReallyCost(Float reallyCost) {
		this.reallyCost = reallyCost;
	}

	public int getStaste() {
		return staste;
	}

	public void setStaste(int staste) {
		this.staste = staste;
	}

	public int getDays() {
		return days;
	}

	public void setDays(int days) {
		this.days = days;
	}

	public int getRegDays() {
		return regDays;
	}

	public void setRegDays(int regDays) {
		this.regDays = regDays;
	}

}
