package cn.sendto.hotel.view;

public class OrderUserModel {
	private int id;
	private int userId;
	private String roomId;
	private String idcard;
	private String cusName;
	private String liveDate;
	private String reallyCheckoutDate;
	private String expectCheckoutDate;
	private Float discount;
	private Float expectCost;
	private Float reallyCost;
	private int staste;
	private int days;
	private int regDays;
	private String userName;
	
	public OrderUserModel() {
		super();
	}

	public OrderUserModel(int id, int userId, String roomId, String idcard,
			String cusName, String liveDate, String reallyCheckoutDate,
			String expectCheckoutDate, Float discount, Float expectCost,
			Float reallyCost, int staste, int days, int regDays, String userName) {
		super();
		this.id = id;
		this.userId = userId;
		this.roomId = roomId;
		this.idcard = idcard;
		this.cusName = cusName;
		this.liveDate = liveDate;
		this.reallyCheckoutDate = reallyCheckoutDate;
		this.expectCheckoutDate = expectCheckoutDate;
		this.discount = discount;
		this.expectCost = expectCost;
		this.reallyCost = reallyCost;
		this.staste = staste;
		this.days = days;
		this.regDays = regDays;
		this.userName = userName;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public String getRoomId() {
		return roomId;
	}

	public void setRoomId(String roomId) {
		this.roomId = roomId;
	}

	public String getIdcard() {
		return idcard;
	}

	public void setIdcard(String idcard) {
		this.idcard = idcard;
	}

	public String getCusName() {
		return cusName;
	}

	public void setCusName(String cusName) {
		this.cusName = cusName;
	}

	public String getLiveDate() {
		return liveDate;
	}

	public void setLiveDate(String liveDate) {
		this.liveDate = liveDate;
	}

	public String getReallyCheckoutDate() {
		return reallyCheckoutDate;
	}

	public void setReallyCheckoutDate(String reallyCheckoutDate) {
		this.reallyCheckoutDate = reallyCheckoutDate;
	}

	public String getExpectCheckoutDate() {
		return expectCheckoutDate;
	}

	public void setExpectCheckoutDate(String expectCheckoutDate) {
		this.expectCheckoutDate = expectCheckoutDate;
	}

	public Float getDiscount() {
		return discount;
	}

	public void setDiscount(Float discount) {
		this.discount = discount;
	}

	public Float getExpectCost() {
		return expectCost;
	}

	public void setExpectCost(Float expectCost) {
		this.expectCost = expectCost;
	}

	public Float getReallyCost() {
		return reallyCost;
	}

	public void setReallyCost(Float reallyCost) {
		this.reallyCost = reallyCost;
	}

	public int getStaste() {
		return staste;
	}

	public void setStaste(int staste) {
		this.staste = staste;
	}

	public int getDays() {
		return days;
	}

	public void setDays(int days) {
		this.days = days;
	}

	public int getRegDays() {
		return regDays;
	}

	public void setRegDays(int regDays) {
		this.regDays = regDays;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}
	
}
